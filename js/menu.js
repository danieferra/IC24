function filtroJS(info){
  var filtros = "";
  switch (info) {
    case '':
      filtros = "sgluten slactose";
      break;
    case 'Lactose':
      filtros = "sgluten";
      break;
    case 'Gluten':
      filtros = "slactose";
      break;
    case 'Gluten Lactose':
        filtros = "";
        break;
    default:
      filtros = "sgluten slactose";
  }
  return filtros;
}

